function [iout] = remove_ibad(i_all,i_bad)
%==========================================================================
%function [iout] = remove_ibad(i_all,i_bad)
%--------------------------------------------------------------------------
% REMIBAD removes the indices 'i_bad' from the vector 'i_all'
%--------------------------------------------------------------------------
% IN: i_all
%     i_bad
%OUT: iout
%--------------------------------------------------------------------------
% EX:
%==========================================================================

for k=1:length(i_bad)
  ind = find(i_all==i_bad(k));
  i_all(ind) = 0;
end

iout = find(i_all~=0);

end