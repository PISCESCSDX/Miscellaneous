function y = logb(x, b)
%==========================================================================
%function y = logb(x, b)
%--------------------------------------------------------------------------
% LOGB Calculates the logarithm of x to the base b.
%--------------------------------------------------------------------------
% IN: x: x vector
%     b: base of the logarithm
%OUT: y: logarithm of x to the base b
%==========================================================================

y = log(x) ./ log(b);

end