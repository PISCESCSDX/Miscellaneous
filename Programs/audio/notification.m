function notification(f,L,noi)
%==========================================================================
%function notification(f,L,noi)
%--------------------------------------------------------------------------
% Jun-07-2013, C. Brandt, San Diego
% Produces a sound.
%--------------------------------------------------------------------------
% f: frequency
% L: seconds
%--------------------------------------------------------------------------
%==========================================================================

if nargin<3; noi=0; end

t = (1:1000*L)'*1e-3;
noise = noi*randn(numel(t),1);
y(:,1) = sin(2*pi*f*t) + noise;
y(:,2) = sin(2*pi*f*t) + [noise(1:end,1)];

% Play sound
Fs = 10000;
sound(y, Fs);

end